/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */
	uint32_t tmp_flag ;
	tmp_flag  = __HAL_UART_GET_FLAG(&huart6, UART_FLAG_IDLE);

	if( tmp_flag  != RESET)
	{
		HAL_UART_DMAStop(&huart6); //停止DMA接收，防止数据出错
		Com_RxLen[6] = USART6_REC_LEN - __HAL_DMA_GET_COUNTER(&hdma_usart6_rx);// 获取DMA中传输的数据个数
		
		//判断数据是否是SUBS协议的数据
		if(Com_RxLen[6] == 25 && USART6_RX_BUF[0] == 0x0f && USART6_RX_BUF[24] == 0x00)
		{
			if(USART6_RX_BUF[23] == 0x00)//没有丢失
			{
				Remote_data.Lost_sta = 1;
				memcpy(sbusbuf,USART6_RX_BUF,25);
			}
			else if(USART6_RX_BUF[23] == 0x0C)//丢失信号
			{
				Remote_data.Lost_sta = 0;
			}
		}
		HAL_UART_Receive_DMA(&huart6,USART6_RX_BUF,USART6_REC_LEN);
	}
	__HAL_UART_CLEAR_IDLEFLAG(&huart6);//清除标志位
  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);
  /* USER CODE BEGIN USART6_IRQn 1 */

  /* USER CODE END USART6_IRQn 1 */
}

